#pip install streamlit

import streamlit as st
import pandas as pd

st.set_page_config(page_title="Flight Connection", layout="wide")

with st.sidebar:
    st.title("Bảng điều khiển")
    uploaded_file = st.file_uploader("Chọn file CSV", type=["csv"])

if uploaded_file is not None:
    df = pd.read_csv(uploaded_file)

    with st.sidebar:
        columns = df.columns.tolist()
        selected_column = st.selectbox("Chọn cột:", columns)
        unique_values = df[selected_column].unique().tolist()
        selected_value = st.selectbox("Chọn giá trị:", unique_values)

def input_info(dataframe, column, value):
    return dataframe[dataframe[column] == value]

def output(processed_data):
    return processed_data

st.header("Flight Connection Application")

if uploaded_file is not None:
    processed_data = input_info(df, selected_column, selected_value)
    result = output(processed_data)

    st.subheader("Kết quả")
    st.dataframe(result, use_container_width=True)

    csv = result.to_csv(index=False)
    st.download_button(
        label="Tải xuống CSV",
        data=csv,
        file_name="result.csv",
        mime="text/csv"
    )
else:
    st.info("Vui lòng tải file CSV để bắt đầu")