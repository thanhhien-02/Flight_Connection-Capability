#pip install streamlit

import streamlit as st
import pandas as pd

# --- CẤU HÌNH TRANG ---
st.set_page_config(page_title="Flight Connection Analysis", layout="wide")

st.title("📊 Flight Connection Analysis")

# --- SIDEBAR ---
with st.sidebar:
    st.header("Cài đặt dữ liệu")

    uploaded_file = st.file_uploader("Tải lên file CSV dữ liệu", type=["csv"])

    if uploaded_file is not None:
        # 1. Đọc dữ liệu gốc
        df = pd.read_csv(uploaded_file)

        # Bước 1: Chọn Year
        list_year = sorted(df['YEAR'].unique())
        selected_year = st.selectbox("1. Chọn Year", list_year)
        df_year = df[df['YEAR'] == selected_year]

        # Bước 2: Chọn Leg (Lọc theo Year)
        list_leg = sorted(df_year['LEG'].unique())
        selected_leg = st.selectbox("2. Chọn Leg", list_leg)
        df_leg = df_year[df_year['LEG'] == selected_leg]

        # Bước 3: Chọn Time Range (CHỈ lọc theo Year và Leg)
        list_timerange = sorted(df_leg['TIMERANGE'].unique())
        selected_timerange = st.selectbox("3. Chọn Time Range", list_timerange)

        # Bước 4: Lựa chọn Inbound/Outbound (Đây là Option, không phải cột trong data)
        selected_type = st.radio("4. Loại hành trình", ["Inbound", "Outbound"])

        st.divider()
        # Nút ấn cuối cùng
        run_button = st.button("🚀 Bắt đầu xử lý")

# --- PHẦN XỬ LÝ LOGIC ---
if uploaded_file is not None:
    if run_button:
        # Lọc dữ liệu thô theo các thông số kỹ thuật (Year, Leg, TimeRange)
        processed_df = df[
            (df['YEAR'] == selected_year) &
            (df['LEG'] == selected_leg) &
            (df['TIMERANGE'] == selected_timerange)
        ].copy()

        if processed_df.empty:
            st.warning("Không có dữ liệu phù hợp với lựa chọn.")
        else:
            # --- ĐIỀU HƯỚNG LOGIC THEO OPTION CHỌN ---
            if selected_type == "Inbound":
                st.subheader(f"Kết quả phân tích: {selected_type}")
                # Chèn logic hoặc gọi hàm inbound(processed_df) của bạn vào đây
            else:
                st.subheader(f"Kết quả phân tích: {selected_type}")
                # Chèn logic hoặc gọi hàm outbound(processed_df) của bạn vào đây

            # --- HIỂN THỊ OUTPUT THEO FORMAT PIVOT ---
            try:
                # Dựa theo code chính: index=['LEG', 'TIMERANGE', 'DEPTIME', 'DURATION'], columns='WEEK', values='LF'
                result_df = processed_df.pivot_table(
                    index=['LEG', 'TIMERANGE', 'DEPTIME', 'DURATION'],
                    columns='WEEK',
                    values='LF'
                ).reset_index()

                # Hiển thị bảng
                st.dataframe(result_df, use_container_width=True)

                # Nút tải xuống
                csv = result_df.to_csv(index=False).encode('utf-8')
                st.download_button("📥 Tải file kết quả CSV", data=csv, file_name=f"{selected_type}_{selected_leg}.csv")

            except Exception as e:
                st.error(f"Lỗi khi tạo bảng Pivot: {e}")
                st.info("Hãy đảm bảo các cột LEG, TIMERANGE, DEPTIME, DURATION, WEEK, LF tồn tại trong dữ liệu.")
    else:
        st.info("👈 Hãy chọn các thông số và nhấn 'Bắt đầu xử lý'")
else:
    st.info("Vui lòng tải lên file CSV để bắt đầu.")