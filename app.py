# time to minutes
def time_to_minutes(time):
    hours = int(time[:2])
    minutes = int(time[2:])
    return hours * 60 + minutes

# minutes to time
def minutes_to_time(minute):
    hours = int(minute // 60)
    minutes = int(minute % 60)
    return f"{hours:02d}{minutes:02d}"

import streamlit as st
import pandas as pd

def inbound_logic(data, leg_input, year, time_range):
    target_arrival_airport = leg_input[-3:]
    original_leg_data = data[(data['YEAR'] == year) & (data['LEG'] == leg_input) & (data['TIMERANGE'] == time_range)].copy()
    if original_leg_data.empty: return pd.DataFrame()

    original_leg_data['Time_Min'] = original_leg_data['ARRTIME'].astype(str).str.zfill(4).apply(time_to_minutes)
    weekly_arrival = original_leg_data.groupby('WEEK')['Time_Min'].mean().reset_index().rename(columns={'Time_Min': 'original_leg_avg_arrival_time'})

    potential_outbound_legs = data[(data['YEAR'] == year) & (data['LEG'].str.startswith(target_arrival_airport))].copy()
    if potential_outbound_legs.empty: return pd.DataFrame()

    potential_outbound_legs['DEPTIME_Min'] = potential_outbound_legs['DEPTIME'].astype(str).str.zfill(4).apply(time_to_minutes)
    grouped_outbound = potential_outbound_legs.groupby(['YEAR', 'WEEK', 'LEG', 'TIMERANGE']).agg(LF=('LF', 'mean'), avg_deptime_min=('DEPTIME_Min', 'mean')).reset_index()

    res = pd.merge(grouped_outbound, weekly_arrival, on='WEEK', how='left')
    res['DURATION_in_minutes'] = (res['avg_deptime_min'] - res['original_leg_avg_arrival_time']).apply(lambda x: x + 1440 if x < 0 else x)
    res['DURATION'] = res['DURATION_in_minutes'].apply(minutes_to_time)
    res['DEPTIME'] = res['avg_deptime_min'].apply(minutes_to_time)

    return res[['LEG', 'TIMERANGE', 'DEPTIME', 'DURATION', 'WEEK', 'LF']]

def outbound_logic(data, leg_input, year, time_range):
    target_departure_airport = leg_input[:3]
    original_leg_data = data[(data['YEAR'] == year) & (data['LEG'] == leg_input) & (data['TIMERANGE'] == time_range)].copy()
    if original_leg_data.empty: return pd.DataFrame()

    original_leg_data['Time_Min'] = original_leg_data['DEPTIME'].astype(str).str.zfill(4).apply(time_to_minutes)
    weekly_deptime = original_leg_data.groupby('WEEK')['Time_Min'].mean().reset_index().rename(columns={'Time_Min': 'original_leg_avg_deptime'})

    potential_inbound_legs = data[(data['YEAR'] == year) & (data['LEG'].str.endswith(target_departure_airport))].copy()
    if potential_inbound_legs.empty: return pd.DataFrame()

    potential_inbound_legs['ARRTIME_Min'] = potential_inbound_legs['ARRTIME'].astype(str).str.zfill(4).apply(time_to_minutes)
    grouped_inbound = potential_inbound_legs.groupby(['YEAR', 'WEEK', 'LEG', 'TIMERANGE']).agg(LF=('LF', 'mean'), avg_arrtime_min=('ARRTIME_Min', 'mean')).reset_index()

    res = pd.merge(grouped_inbound, weekly_deptime, on='WEEK', how='left')
    res['DURATION_in_minutes'] = (res['original_leg_avg_deptime'] - res['avg_arrtime_min']).apply(lambda x: x + 1440 if x < 0 else x)
    res['DURATION'] = res['DURATION_in_minutes'].apply(minutes_to_time)
    # Đổi tên ARRTIME thành DEPTIME để bảng Pivot hiển thị đồng nhất cột thời gian
    res['DEPTIME'] = res['avg_arrtime_min'].apply(minutes_to_time)

    return res[['LEG', 'TIMERANGE', 'DEPTIME', 'DURATION', 'WEEK', 'LF']]

# --- CẤU HÌNH TRANG & SIDEBAR ---
st.set_page_config(page_title="Flight Connection Analysis", layout="wide")
st.title("📊 Flight Connection Analysis")

with st.sidebar:
    st.header("Cài đặt dữ liệu")
    uploaded_file = st.file_uploader("Tải lên file CSV dữ liệu", type=["csv"])

    if uploaded_file is not None:
        data = pd.read_csv(uploaded_file)
        data.columns = data.columns.str.strip() # Xóa khoảng trắng tiêu đề cột

        selected_year = st.selectbox("1. Chọn Year", sorted(data['YEAR'].unique()))
        df_year = data[data['YEAR'] == selected_year]

        selected_leg = st.selectbox("2. Chọn Leg", sorted(df_year['LEG'].unique()))
        df_leg = df_year[df_year['LEG'] == selected_leg]

        selected_timerange = st.selectbox("3. Chọn Time Range", sorted(df_leg['TIMERANGE'].unique()))
        selected_type = st.radio("4. Loại hành trình", ["Inbound", "Outbound"])

        st.divider()
        run_button = st.button("🚀 Bắt đầu xử lý")

# --- PHẦN XỬ LÝ VÀ HIỂN THỊ KẾT QUẢ ---
if uploaded_file is not None:
    if run_button:
        # GỌI HÀM LOGIC (Thay vì chỉ định nghĩa, ta thực thi để lấy dữ liệu có cột DURATION)
        with st.spinner('Đang tính toán kết nối...'):
            if selected_type == "Inbound":
                final_df = inbound_logic(data, selected_leg, selected_year, selected_timerange)
            else:
                final_df = outbound_logic(data, selected_leg, selected_year, selected_timerange)

        if final_df.empty:
            st.warning("Không tìm thấy dữ liệu kết nối phù hợp.")
        else:
            try:
                # Tạo bảng Pivot từ dữ liệu đã qua xử lý (final_df)
                result_pivot = final_df.pivot_table(
                    index=['LEG', 'TIMERANGE', 'DEPTIME', 'DURATION'],
                    columns='WEEK',
                    values='LF'
                ).reset_index()

                st.subheader(f"Kết quả phân tích: {selected_type}")
                st.dataframe(result_pivot, use_container_width=True)

                # Nút tải xuống
                csv = result_pivot.to_csv(index=False).encode('utf-8')
                st.download_button("📥 Tải file kết quả CSV", data=csv, file_name=f"{selected_type}_{selected_leg}.csv")

            except Exception as e:
                st.error(f"Lỗi khi tạo bảng Pivot: {e}")
                st.info("Lưu ý: Đảm bảo dữ liệu thô có đủ các cột YEAR, LEG, WEEK, LF, ARRTIME, DEPTIME.")
    else:
        st.info("👈 Hãy chọn các thông số và nhấn 'Bắt đầu xử lý'")
else:
    st.info("Vui lòng tải lên file CSV để bắt đầu.")