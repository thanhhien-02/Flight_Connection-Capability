#pip install streamlit

import streamlit as st
import pandas as pd

# --- CẤU HÌNH TRANG ---
st.set_page_config(page_title="Flight Connection Analysis", layout="wide")

st.title("📊 Ứng dụng Phân tích Kết nối Chuyến bay")
st.markdown("Tải lên file dữ liệu CSV và chọn các thông số để xem kết quả phân tích.")

# --- SIDEBAR: CÀI ĐẶT THÔNG SỐ ---
with st.sidebar:
    st.header("Cài đặt dữ liệu")

    # 1. Upload file CSV
    uploaded_file = st.file_uploader("Tải lên file CSV dữ liệu (VD: Result_17.csv)", type=["csv"])

    if uploaded_file is not None:
        # Đọc dữ liệu ban đầu
        df = pd.read_csv(uploaded_file)

        # 2. Chọn Year
        years = sorted(df['YEAR'].unique()) if 'YEAR' in df.columns else []
        selected_year = st.selectbox("Chọn Year", years)

        # 3. Chọn Loại hành trình (Giả định cột đặt tên là INOUT dựa trên yêu cầu)
        # Nếu file của bạn dùng tên cột khác, hãy đổi tên 'INOUT' ở dưới
        inout_col = 'INOUT' if 'INOUT' in df.columns else (df.columns[df.columns.str.contains('INOUT|bound', case=False)].tolist() + [None])[0]

        if inout_col:
            inout_options = df[inout_col].unique()
            selected_inout = st.selectbox("Loại hành trình (Inbound/Outbound)", inout_options)

        # 4. Chọn Leg
        legs = sorted(df['LEG'].unique()) if 'LEG' in df.columns else []
        selected_leg = st.selectbox("Chọn Leg", legs)

        # 5. Chọn Time Range
        timeranges = sorted(df['TIMERANGE'].unique()) if 'TIMERANGE' in df.columns else []
        selected_timerange = st.selectbox("Chọn Time Range", timeranges)

        # 6. Nút ấn cuối cùng
        run_button = st.button("Bắt đầu xử lý dữ liệu")

# --- PHẦN XỬ LÝ LOGIC VÀ HIỂN THỊ ---
if uploaded_file is not None:
    if run_button:
        try:
            # Bắt đầu lọc dữ liệu dựa trên lựa chọn của người dùng
            filtered_df = df[
                (df['YEAR'] == selected_year) &
                (df['LEG'] == selected_leg) &
                (df['TIMERANGE'] == selected_timerange)
            ].copy()

            if inout_col:
                filtered_df = filtered_df[filtered_df[inout_col] == selected_inout]

            if filtered_df.empty:
                st.warning("Không tìm thấy dữ liệu phù hợp với các lựa chọn trên.")
            else:
                # Thực hiện Logic Pivot giống trong file Notebook
                # Index bao gồm: LEG, TIMERANGE, DEPTIME, DURATION
                # Columns: WEEK
                # Values: LF
                result_df = filtered_df.pivot_table(
                    index=['LEG', 'TIMERANGE', 'DEPTIME', 'DURATION'],
                    columns='WEEK',
                    values='LF'
                ).reset_index()

                # HIỂN THỊ KẾT QUẢ
                st.subheader(f"Kết quả phân tích cho {selected_leg} ({selected_year})")
                st.dataframe(result_df, use_container_width=True)

                # Nút tải xuống kết quả
                csv_data = result_df.to_csv(index=False).encode('utf-8')
                st.download_button(
                    label="Tải kết quả xuống CSV",
                    data=csv_data,
                    file_name=f"result_{selected_leg}_{selected_year}.csv",
                    mime="text/csv",
                )
        except Exception as e:
            st.error(f"Có lỗi xảy ra khi xử lý dữ liệu: {e}")
            st.info("Vui lòng kiểm tra lại cấu trúc các cột trong file CSV (LEG, TIMERANGE, WEEK, LF, YEAR...).")
    else:
        st.info("Vui lòng chọn các thông số ở Sidebar và nhấn 'Bắt đầu xử lý dữ liệu'.")
else:
    st.info("👈 Vui lòng tải file CSV ở thanh bên trái để bắt đầu.")