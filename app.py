#pip install streamlit

import streamlit as st
import pandas as pd

st.set_page_config(page_title="Flight Connection", layout="wide")

with st.sidebar:
    st.title("Bảng Điều Khiển")

    uploaded_file = st.file_uploader("Chọn file CSV", type=["csv"])

    if uploaded_file is not None:
        df = pd.read_csv(uploaded_file)
        df = df.rename(columns={"WEEK_NUMBER": "WEEK", "H2": "TIME RANGE"})

        st.write("---")
        year = st.selectbox("Chọn năm:", sorted(df['year'].unique()) if 'year' in df.columns else [])
        trip_type = st.selectbox("Loại hành trình:", sorted(df['trip_type'].unique()) if 'trip_type' in df.columns else [])
        leg = st.selectbox("Leg:", sorted(df['leg'].unique()) if 'leg' in df.columns else [])

        time_range = st.slider("Chọn khoảng thời gian:",
                               min_value=int(df['TIME RANGE'].min()),
                               max_value=int(df['TIME RANGE'].max()),
                               value=(int(df['TIME RANGE'].min()), int(df['TIME RANGE'].max())))
    else:
        df = None
        st.info("Vui lòng tải file CSV")

def input_info(dataframe, year, trip_type, leg, time_range):
    if dataframe is None:
        return None

    filtered_df = dataframe[
        (dataframe['year'] == year) &
        (dataframe['trip_type'] == trip_type) &
        (dataframe['leg'] == leg) &
        (dataframe['TIME RANGE'] >= time_range[0]) &
        (dataframe['TIME RANGE'] <= time_range[1])
    ]

    return filtered_df

def output(data):
    if data is None or len(data) == 0:
        return {"status": "Không có dữ liệu", "data": None}

    return {
        "status": f"Tìm thấy {len(data)} bản ghi",
        "data": data
    }

st.header("Flight Connection Capability")

if df is not None:
    filtered_data = input_info(df, year, trip_type, leg, time_range)
    result = output(filtered_data)

    st.subheader(result['status'])

    if result['data'] is not None and len(result['data']) > 0:
        st.dataframe(result['data'], use_container_width=True)

        csv = result['data'].to_csv(index=False)
        st.download_button(
            label="Tải xuống CSV",
            data=csv,
            file_name="flight_data.csv",
            mime="text/csv"
        )